<p>
鹿港有许多旧式的建筑还在使用中，使整个小镇颇具古意，比大陆许多县镇一排排的新房子要美太多。走到鹿港古迹保护区瑶林街处，更是如此，同时，由于旅游业的发展，不宽的巷子中有不少在自家门口卖纪念品和特产的小摊。小镇北端，现在世界上唯一存有湄洲妈祖本尊的天后宫有近四百年历史，庙宇不大，但陈设紧凑精致，有不少当地人前来参拜。小镇南端，龙山寺是台湾一级古迹，同样有近四百年历史，其清朝建筑物保存完整，远看即顿觉斑驳之意。至于很有名的摸乳巷，名称来源是据传巷子一端异常狭窄，男女相遇则可借机轻薄。可惜我先后两次走过此巷，都没能有此遭遇。
