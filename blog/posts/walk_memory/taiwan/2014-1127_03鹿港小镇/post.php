<p>
阿姗以及另一位姑娘合开了这家青年旅舍，两人是国中（初中）同学，也经历过离乡读书，最后又回到家乡，于两年前开了鹿港第一家青年旅舍。两人均住在自己家里，每天过来数次，处理各种繁杂事务。今天早上阿姗给我们做了三明治当早餐，除了鸡蛋之外，还放了苹果和奇异果，以及自制的果酱，极为美味，美中不足的是，将一个切为两半，对于我而言，半个份量略显不足啊。我就着咖啡风卷残云之后，阿姗看另一位客人还没下楼，就用保鲜膜小心地把他那份包好。
</p>

<p>
大概是由于女孩子操持的原因，这家青年旅舍相当干净，地板上尽可赤足来回。并不宽敞的一楼大厅，一书架的书，满墙的各种地图，零零散散的书法作品，各种照片贴成的一幅台湾图，居中的长桌前，阿姗正在娓娓讲述鹿港的沿革史：鹿港历史上曾盛极一时，是台湾的三大都市之一，后来港口淤塞，盛况渐衰，中日战争爆发后与大陆贸易断绝，成为废港……
