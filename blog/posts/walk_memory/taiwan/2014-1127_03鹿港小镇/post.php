<p>
2014.11.27. 晴
</p>

<p>
昨天上午，出了台中港，就是公交站，可以坐车去台中市区。有台中港的工作人员自发准备了零钱，换给只有大额台币、无法坐公交车的乘客，并热情指导各人如何到达目的地。我第一站想去位于台湾西部、隶属于彰化县的鹿港镇。昨晚与我住同一间的台湾大叔，与我一同坐公交。到换乘BRT处，大叔说陪我坐车去火车站，帮我找去鹿港的车。他说，下午要去公司述职，不然就开车送我去鹿港了。
</p>

<p>
到达火车站后，附近虽有不少客运站，但去鹿港的车还真是不好找。初到台湾的我，直感一头雾水。好在大叔很耐心地询问各处，终于在走出好几百米后找到了直达鹿港的公交车站。大叔祝我旅途愉快之后，即转身快步离去。
</p>

<p>
使用刚办的悠游卡刷卡上车，经过一个多小时，到达鹿港镇，下车时驾驶员还特地指给我看哪里是返程的乘车点。正午时分，按照《Lonely Planet》的地图找到缩在巷子中的Tai 17 Backpacker青年旅舍，推门进入一楼客厅，老板却不在。依留言条打电话给老板，一个年轻姑娘接的电话，说让我稍等十分钟。不久，二十多岁神似邻家姐姐的老板阿姗现身了，递给我一张鹿港的景点地图，给我讲起各种景点街巷以及各色小吃。足足介绍了近二十分钟，在地图上将重点推荐之处都圈点备注，才放我出门去吃午饭。后来，阿姗告诉我，之前有个客人来的时候她不在，让客人先自己出去逛一逛，结果她回来时，客人说，我要走了，刚才逛了一下，鹿港挺好的。之后，阿姗都会给客人介绍，以免客人因不知道鹿港有哪些可看可吃的而失望离去。
</p>

<p>
鹿港是一个被游客打扰较少的小镇，整体安静且干净，很适合漫步其中，享受小镇的悠闲。地处小镇中心位置的菜市场有很多小吃，我按图索骥，找最热门的名铺下口。看到一家面线糊有很多人在吃，也进去点了一份。与在厦门吃的不同，此面线糊的面线极细，浇入猪肉、虾仁等，啜入口中柔腻而不失滑爽。吃着吃着，发现坐在店里的客人多似游客，顿时觉得没那么好吃了。嘿，说了句玩笑话，平心而论，还是挺不错的。龙山鱿鱼肉羹也很有特色，用笋丝、鱿鱼、猪肉炖成，鱿鱼很香，笋丝很鲜。另一家筒仔米糕的店生意极好，傍晚时分门口排起了长队，我跟着一位常客要了一个筒仔米糕加一份四神汤。筒仔米糕是将肉丁等放在已加料炒过的糯米上，置于筒中蒸制而成，香味浓郁，我一向喜欢吃糯米的食物，自是吃得津津有味。而四神汤是用所谓莲子、薏仁（原为淮山）、芡实、茯苓这中药四臣，再加猪小肠煮制而成的“月子餐”，具有非同一般的调和身体的功效，而这一道四神汤中，猪小肠清洗得极为干净，柔嫩得几乎入口化开，一回味，满口都是中药的清香，令人拍案叫绝！
