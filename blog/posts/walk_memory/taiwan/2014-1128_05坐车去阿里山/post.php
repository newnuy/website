<p>
到了嘉义，出站后，本可以坐公交车去阿里山，250台币的票价。恰恰有面包车正在拉客去阿里山，愿意与公交车相同的价格载我，而预计开车时间则要早很多。我当然乐得选择这种方式了。虽然到了预计时间并未完全载满客，面包车还是按时出发了。在两个多小时的行程中，驾驶员一路毫不停歇地介绍阿里山的方方面面，我听得都为他感到嘴巴好累，然后我就昏昏入睡了。
</p>

<p>
在中途下车休息的时间，我打电话预订好天主教堂的床铺。到阿里山入口购买门票时，因为是驾驶员代买，别人的学生证重复作用到了我身上，卖给我一张100台币的学生票，省了50台币，我说“我不是学生”，也没人理我。
</p>

