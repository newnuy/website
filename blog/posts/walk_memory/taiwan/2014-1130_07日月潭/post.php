<p>
缆车终点是九族文化村的入口，门票为780台币。其实倒也不算贵，但我一者对带有各种游乐设施的景点兴趣不大，二者也不愿在团客拥挤之处多做停留。因此坐缆车原路返回伊达邵码头，再坐游船回到起点处的水社码头。此处有往返于台湾各地的巴士，我迅速买好去台中的车票，离开了这嘈杂之地。
</p>

