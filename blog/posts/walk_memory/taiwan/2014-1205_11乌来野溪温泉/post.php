    台北机车车流，同时旁边等公交的队伍令人惊叹台湾人的秩序
    </div>
</p>

<p>
旁边一位约莫20岁的姑娘赞同我的话。大叔又开始考较那姑娘，我笑着对姑娘说：你也读书少对吧？姑娘认真地告诉我：我读书不少。不过很快她也开始用“我读书少”来回答大叔，与我形成了共同无视大叔的默契。大叔的口头禅也由“你还是太年轻”变成了“说你读书少就是读书少”。
</p>

<p>
这位姑娘是青旅的工作人员，她的名字很特别，意闲，与众不同的恬淡。意闲说起笔记本很卡，我说Mac用很多年都不会卡，真心推荐。意闲问起手机有什么推荐的，我说iPhone。意闲说自己是一个乐于分享的人，但苹果是一个封闭的体系，所以不愿意用。我说，换一个角度看问题，或许会有不同，比如iPhone因为软硬件一体化，以低得多的硬件配置提供更好的性能，从功耗的角度来说是不是更环保呢？Mac系统所属的Unix体系及后来的Linux，正因为开源而使得更好的软件被分享，此刻全球数以百万千万计的Unix/Linux服务器为人们提供了各种服务。西班牙语专业的意闲说：你好像说服我了耶。大陆的不少女孩子有一个有趣的现象，在自己不了解的领域内，常常表现得毫无兴趣。台湾的不少女孩子会愿意去倾听去思考。
</p>

<p>
我说起想去乌来，意闲在大厅的一个角落翻出一张乌来的旅游图给我。问我为什么想去乌来，意闲说去乌来的游客并不多。台湾有不少著名的温泉，台北附近最著名的当属北投了。来台湾前Sunber说起乌来的野溪温泉，即令我神往不已，而刚到花莲时，全台气温陡降，玉山合欢山下起了雪，而在乌来的野溪温泉，当地的老人家在溪中游泳，然后泡温泉，再去游泳，如此往复，充分享受了冰火两重天的境界。看到这段电视新闻，北投与乌来之间我不再犹豫，我要去泡野溪温泉！
</p>

<p>
家乡在高雄的意闲说她不知道乌来有野溪温泉，问我可以过一周，等她一起去吗？我说10号我就要离开台湾啦，你这几天可以请一天假，我们一同去，享受一下和当地的老人家一起泡温泉的感觉。意闲说原本在这边就是兼职，不好意思请假。顿了一下又说：老人家才不愿意和你一起呢。我不禁莞尔。
</p>

<p>
到了台北，就开始觉得特别困，今天一觉睡到临近中午，出发前往乌来。乌来位于新北市的范围内了，需要先坐捷运，再转公交。经过约一个半小时，终于到达乌来。乌来属于山林地区，下车后沿着溪水上行，数分钟就到了乌来老街。
</p>

<p>
乌来因温泉而闻名，有着许许多多的温泉旅馆，价格也着实便宜，一两百台币就可以去泡几小时。在这游客络绎不绝的老街上最多的当属各种餐馆，正好解救我当前的辘辘饥肠。进了一家餐馆，点了一份竹筒饭，一份羊肉片汤，共160台币，还算便宜。老板还特别告诉我竹膜是可以吃的。竹筒饭清香，羊肉片汤生劲，吃完之后，就该去找野溪温泉了。
