</p>

<p>
回到台北，早已到了晚饭时间。去尝一尝有名的金峰鲁肉饭吧，在捷运出口处的这家店并不大，拥挤的座位难挡大家排队的热情。我要了鲁肉饭、凤梨苦瓜鸡、笋干，才110台币，鲁肉饭香而不腻、入口柔润，凤梨苦瓜鸡以一个完整的三段鸡翅佐以苦瓜炖成，清香而去火，笋干鲜嫩味美，一天的疲劳在美食相伴中尽消。
