</p>

<p>
走到老街尽头，过桥到溪的另一边，再往回走一段，就看到了溪边的人头攒动。赶紧加快脚步，走近看清溪边有围了大大小小的池子，温泉水被引入池子，热气蒸腾下，老少男女或坐或站，浸身其中。这种幕天席地的场景，令我放松下来。有人在池边搭了很简陋的更衣室，我进去脱光衣服换上泳裤，挑了一个老人家较多的池子，跨进池子里慢慢坐下。烫！爽！
