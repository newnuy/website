<p>
2014.12.05. 阴
</p>

<p>
昨天下午，到达台北，下了火车即感受到台北的节奏。此处的台北转运站，连通火车站、高铁站、长途车站、捷运（地铁）站，布局紧凑复杂，跟着指示牌七弯八绕，完全迷失了方向。寻到一个出口，来到室外，台北好冷，寒风呼呼，来来往往的行人多数裹衣疾行，而其中妆容精致、身形窈窕的女孩子比例极高，而其中戴口罩的比例也极高。
</p>

<p>
不多时就找到了紧临火车站的这家名为“夹脚拖的家”的青年旅舍，从门面来看，似乎颇为现代化。这家青旅很有意思，进门必须脱鞋。虽然是地板，可这几天天气寒冷，我这些年即使是夏天也不光脚，此时穿了袜子仍觉得冷。工作人员告诉我鞋柜里有拖鞋，好大一个鞋柜，我打开一看，各种夹脚拖（人字拖），只能默默关上柜门，谁让我穿不惯这种拖鞋呢，只能忍一忍地上的寒气了。
</p>

<p>
我被安排到一个六人间，素雅坚固的双层床，柔软洁净的被子，空间庞大的床头置物柜，明亮的床头阅读灯，挂于床侧的衣架，算是我住过的设施最齐全贴心的青年旅舍了。地板确实如工作人员所说，每天都会清洁，非常干净。差不多接近晚饭的时间了，正好下楼去大厅坐一会儿。
</p>

<p>
恰好大厅有个从深圳来旅游的大叔，听到我也是来自深圳，马上开始考较我的台湾见闻。他开口闭口就是“你还是太年轻”，我只能回答“我读书少，你不要问我了。”大叔抓着我不放，又盛赞台北的交通便利，我说，台北交通便利，但也并非尽如人意，比如风驰电掣的机车，就挺危险，而且污染严重。大叔坚称机车很好，我说，我问过不少台湾人，我刚才说的是他们的想法，你才来台湾几天，能不能听听当地人的声音？
