</p>

<p>
没有如意闲所说，老人家不是不愿意和我一起，而是根本没有注意到我。他们用方言聊天，我也完全听不懂。在一个池子泡了一会儿，觉得气闷时，就起来吹一吹风，换个池子。如此换过几个池子之后，渐觉疲惫，看到有老人家在不远的溪水中游泳，我想临近池子的溪水是不是热的，我也可以下去试一下。探足一试，完全是冷的，这个天气，冰火两重天还是不要尝试了，弄不好发烧了，到时忽冷忽热的，却也是两重天。
</p>

<p>
大约泡了一个小时的野溪温泉，体力已臻极限，于是起身去换回衣裤，到不远处体验一把台车。所谓台车，是指轻便铁路上的火车。乌来的台车可以从此处坐到乌来瀑布，50台币的价格换来步行20分钟的上山路程，算得上便宜。因为台车不是封闭的，在窄窄的铁路上疾驰，也算一种特别的体验了。至于乌来瀑布，也并不特别，远观一下足矣。
    <div class="i_center">
    <img src="/wp-content/uploads/2015/01/taipei005.jpg" width="640" title="" /><br />
    台车
    </div>
</p>

<p>
从乌来瀑布一路下行，不多时回到老街，这时游客更多，烤山猪肉引得不少游客驻足购买。有一种烤山猪踺子肉，100台币一串，看起来很大一串，不知道是不是特别美味。我问旁边一位台湾的姐姐，她说她们几个人买了一串，正在等，是否好吃还不知道。我说，等你们吃了觉得好吃我再买。不久，她们的烤好了，这位姐姐撕下一块给我，让我尝尝看，她们则边吃边走远了。我尝了觉得比较硬，味道也一般，就没有买。
</p>

<p>
老街上有一个乌来泰雅民族博物馆，我进去后跟着一个学生团蹭讲解。这位讲解员如老师一般，详尽而谆谆善诱，还不时出题给大家回答，答对了有小奖品。其中很大篇幅在讲述泰雅族纹面习俗，其信息的翔实准确，直听得我实在是站不动了。
