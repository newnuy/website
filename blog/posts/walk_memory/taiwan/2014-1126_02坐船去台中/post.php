    <a href="/wp-content/uploads/2014/11/taichung005.jpg" target="_blank"><img src="/wp-content/uploads/2014/11/taichung005.jpg" width="640" title="" /></a><br />
    临近台中港时看到的灯塔
    </div>
</p>

<p>
不久，停靠台中港，下船，通过安检后，就真正进入了台湾。
</p>

