<p>
吃完冰淇淋，时间也差不多了，慢慢踱到古色古香的台中火车站，检票上车。普悠玛号列车行驶时的左右摇晃让我大吃一惊，读书看报都容易头晕。旁边的姐姐告诉我，这是由日本制造的倾斜式列车，在行驶过弯道时较非倾斜式列车速度快，但不可避免的就是乘客容易晕车，因而大多数乘客都是在闭目养神。
    <div class="i_center">
    <img src="/wp-content/uploads/2014/12/taichung009.jpg" width="640" title="" /><br />
    台中火车站
    </div>
</p>

<p>
除了晃动引起的声响，没有伴随着吆喝卖食品的推车，车厢内很安静，实在比深圳的电影院体验好很多。我睡了一个多小时后醒来，着实饿了，正好听广播中说有便当卖，赶紧找乘务员买了一份。打开纸盒一看，好大一块大排，蔬菜搭配也很得宜，吃起来味道还算不错。而这一个便当才60台币，仅相当于12人民币。而就算在阿里山上，各种饮料的价格也与其它地方无异。遍布台湾的7-Eleven便利店，24小时营业，除了各种平价的商品外，还提供手机卡加值、悠游卡售卖等服务，甚至推出了自己的手机卡。许多7-Eleven另一个非常惠民之处是提供了洗手间。
    <div class="i_center">
    <img src="/wp-content/uploads/2014/12/taichung010.jpg" width="640" title="" /><br />
    火车上的便当
    </div>
</p>

<p>
旁边的姐姐轻声细语地给我介绍起花莲及附近的景点来，最著名的当属太鲁阁国家公园了。姐姐详详细细地说起太鲁阁的各个景点及相应住宿，并手绘路线给我看。去太鲁阁，最便捷的方式当属开车或骑机车（摩托车）了，可我既不会开车，也不会骑机车。姐姐说，机车和脚踏车差不多的，应该不难学会的。我说，我所在的城市机车不允许上道，所以我也没有机会去学，当然也就没有驾照了。在台湾，会骑机车委实方便太多。花莲南北的一些地方，姐姐也一一给我介绍，时间变得快速起来，临到站时，姐姐关照我，一定要去尝一尝炸弹葱油饼，有两家，蓝色车子和黄色车子，要去买蓝色车子的那一家哦。
</p>

<p>
花莲市隶属于台湾东部沿海的花莲县，市区并不算大。下了火车，走到出站口，工作人员要检查回收火车票，我问：我想收藏这车票可以吗？工作人员很亲切地把票递回我手中说：可以的。今天降温了许多，花莲下起了小雨，穿上外套仍觉得寒冷。好在这家阿美客青年旅舍离火车站很近，步行数分钟即寻到。一楼大厅内，一群人围着茶几看电视聊天，我也顿时觉得温暖热闹起来。
</p>

