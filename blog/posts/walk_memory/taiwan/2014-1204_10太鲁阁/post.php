    <img src="/wp-content/uploads/2015/01/hualien010.jpg" width="640" title="" /><br />
    铁路鸡腿便当
    </div>
</p>

<p>
到花莲火车站时，离出发时间尚有十几分钟，就在候车厅坐下。旁边座位上来了一个约五十岁的男子，衣服不算干净整齐，可能是附近的建筑装修的工人。他坐下后，开始吃手中的便当，很快吃完，并将另一边座位上前面人落下的一次性筷子的包装纸也一并放入塑料袋中，不声不响地去找垃圾筒。
</p>

<p>
如果说太鲁阁给我山水上的失望，那这一幕则给了我人文上的惊喜和震撼。台湾所有的公共厕所都非常干净，即使是像火车站这种人来人往的地方。各种站牌、墙角，看不到一处牛皮癣。大街小巷的垃圾筒很少，往往需要来回寻找，但几乎在地上看不到垃圾，小镇上的角落也处处干净。台湾的环境，不是清洁工人打扫出来的，而是市民整体素质使然。
</p>

