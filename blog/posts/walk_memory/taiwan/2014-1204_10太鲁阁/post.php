<p>
2014.12.04. 小雨
</p>

<p>
在《Lonely Planet Taiwan》的Top15中，排在第一的是Hiking the High Mountains，这个我还是不要去招惹了。排在第二的是Taroko Gorge，译成中文则是“太鲁阁峡谷”，一般来说会称为太鲁阁国家公园，配图中峡谷绿水的美景打动了我，是我此行台湾最想去的地方。
</p>

<p>
昨天我去了太鲁阁。对于花莲人，也许习惯于要么开车要么骑机车，对于游客，还是有班车可以坐的。从花莲火车站坐班车进入太鲁阁，在其内各站可以下车游览，大约过一小时再乘下一趟班车继续前行即可。班车往返套票250台币。
</p>

<p>
进入太鲁阁需要40分钟，太鲁阁内共有7站，两两之间不超过5分钟的车程。第一站是砂卡礑，此处有数公里长的砂卡礑步道，看描述为景色秀丽之处，以溪水和植物为特色。第二站是布洛湾，此处有太鲁阁族工艺展示馆，以工艺作品的方式展示了太鲁阁族的一些生活习性，最有特色的当属文面习俗：在十四五岁时，男子猎得第一个人头即可刺上颐纹，女子纺织技艺娴熟即可刺上颊纹，以表示长大成人，具有成家立业的资格。
</p>

<p>
布洛湾附近有个环流丘景观公园，有一些步道建于森林之中，可以俯瞰溪水峡谷。不过公园很小，须臾就走遍了，而下一趟班车还要等挺久，于是我萌生了沿步道前往下一站燕子口的念头。当地人居然告诉我走到燕子口还要很久，实际上沿着林中步道一路向下，下到公路上，再前行一段，二十分钟就可到达。
