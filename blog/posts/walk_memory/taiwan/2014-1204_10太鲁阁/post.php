</p>

<p>
燕子口景致优美，有的角度看到的峡谷与《Lonely Planet》中的配图相似。气人的是，隧道因修缮不可步行通过，一座横跨峡谷的铁索桥也不开放。此处只能等班车，而下一站九曲洞的九曲洞步道，是太鲁阁峡谷最精华的路段，同样不开放。再后面就是绿水、天祥两站，接下来返程，会停一站长春祠，我都没有下去看。若说山水之险秀或壮丽，将太鲁阁放在大陆的景点中，只怕很难从黄山五岳九寨沟等中脱颖而出。
