<p>
从太鲁阁回到花莲，在市区信步走走，在一闹中取静的所在，看到一家时光二手书店。古旧却盎然春意的一层平房，里面的空间却不小呢，各种各样的书分类而陈，几个年轻人很安静地或站或坐，细细品读。网络的出现，对纸质书的冲击令人心寒，大陆很多人开各种书吧都面临经营不下去的困境。希望台湾的环境，能够让店主的坚持得以维系。
    <div class="i_center">
    <img src="/wp-content/uploads/2015/01/hualien008.jpg" width="640" title="" /><br />
    闹中取静的时光二手书店
    </div>
</p>

<p>
不知道台湾的刀削面有何不同，于是晚饭我来到了这家花莲鼎鼎有名的顺富裕刀削面。这家店不在最热闹的街市上，却人满为患。我点上一份肠旺羊肉刀削面，再配上一碟招牌饼和一碟海带卷，总价160台币。刀削面中放了猪血还是什么血的，并且做得如此浓稠的，真是第一次见，吃上一口，甜的，招牌饼也是甜的。在这满嘴的甜味中，慢慢地享用完了这一餐。看来，面食还是得去西北，寒风黄土，或许才能孕育出有劲道的面食。
    <div class="i_center">
    <img src="/wp-content/uploads/2015/01/hualien009.jpg" width="640" title="" /><br />
    顺富裕的肠旺羊肉刀削面，招牌饼，海带卷
    </div>
</p>

<p>
今天睡了个懒觉，临近中午时退房离开，准备前往台北了。先步行到几分钟远的花莲火车站，买好去台北的票，还有一个多小时，去邮局寄个物件，再吃完午饭，正好回来乘车。刚出火车站，有一个年轻人手持一张卡片，向我示意。我看到卡片上写租机车，于是摇摇头，他大概以为我不明白他的意思，又对我轻声说租机车。我说，我马上要离开花莲了，不是刚来的游客。年轻人满脸惶恐，连连对我说对不起打扰了。
</p>

<p>
我昨天才发现在鹿港的<a href="/?p=1387" target="_blank">T17 Backpacker</a>，退房时忘记归还钥匙了。昨晚写邮件给阿姗，为此致歉，故意没有提把钥匙寄给她，想看看她会如何回复。今早收到阿姗的回信，是这样写的：<span class="q_kai">如果你還在台灣的話，可以幫我把鑰匙寄來嗎? (我聽一個大旅行家說：要觀察一個地方可以從他的郵政效率開始....  這樣有沒有想去郵局看看XD ) 如果你已經出境的話，那就等你下次再來吧:)) </span>不得不赞叹阿姗的说话艺术。
</p>

<p>
到了邮局，工作人员亲切的态度让我感觉很不适应，竖写的信封也让我很不适应，信封背面详细列出了台湾各地区的邮编。用简体填完信封上的信息，将钥匙置入，糊上胶水，交给工作人员，称重之后共付31台币。
</p>

<p>
接下来正好再去尝一尝另一家铁路便当，离邮局也比较近。这次我要了一份75台币的鸡腿便当，人民币15元在深圳已经不容易吃到可口的盒饭了。这次在店里吃，用沉甸甸的铁便当盒来盛的，有一种是自己家做的便当的感觉。店员还不忘提醒我，换个方向坐可以边吃边看电视。菜的量很足，且新鲜爽口，与国内盒饭菜少味薄形成了鲜明的对比。
