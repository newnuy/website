</p>

<p>
兰州的交通很是神奇，今天星期二，非上下班高峰期，私家车限号的情况下，居然仍堵得厉害，真是难以想象高峰期又是怎么一种情况！旅行途中，也常常需要依靠手机地图，大家首推百度地图，确实，新版的谷歌地图已经让我不会使用了。但是，很多人和我说百度地图做得比谷歌地图精准，我不敢苟同，百度地图曾指示我步行从厦门市区到鼓浪屿，从广州东站到广州机场花4小时坐高铁。
</p>

<p>
中午继续睡了一会儿，就离开兰州，前往甘南藏族自治州。近年来，这块土地吸引了越来越多人的注意，是一个不用到西藏就可以领略藏区风情的所在。那么，甘南的第一站，夏河县，所谓“小西藏”。
</p>

