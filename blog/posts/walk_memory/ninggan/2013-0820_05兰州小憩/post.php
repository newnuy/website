<p>
2013.08.20. 多云
</p>

<p>
昨天凌晨，在固原到兰州的绿皮火车的硬座车厢，实在是有点睡不着。于是和对面的一个姑娘开始聊天，天南海北。90年的姑娘，说自己还小，不禁让我想起前几天遇到的那个92年的姑娘，也是说自己还小。92年的姑娘曾说：她男朋友对她非常非常好，在一起两年了，她仍对男朋友很有感觉，但却似乎并不懂得珍惜。是呢，世道人心，就是那样奇特；机缘命运，又总是那样弄人，在你不怎么需要的时候硬塞给你，让你不忍拒绝，却又殊难惜视，到了你需要的时候，却又怎么都不出现，让你苦苦期盼。
</p>

<p>
在半夜时，终于全车厢安静下来，大家一起睡了那么两三小时。之后就是呆呆观察清晨的窗外色调的变化，然后等来了火车到站。去寻找一家评价挺不错的青年旅舍，打电话没人接，问了一些人之后，好容易寻到，敲开门却被告知近期在停业装修。看看时间也接近中午了，就近找了一家普通宾馆，在附近随便吃了午饭，开始恶补睡眠。昨天一夜没怎么睡，我决定不在兰州游玩，先把精神恢复好。
</p>

<p>
我的整个旅途其实要比大多数旅行者更累，主要是基本每晚都要写博客，不能积压，由此产生的压力很大。到了晚饭时间，我出门去，寻到传说中物美价廉的巧嫂子，点了一份兰州糟肉和荞面搅团。荞面搅团是不是可以理解成加硬版的荞面糊，糟肉是不是可以理解成小清新版的扣肉？口感确实都很有特点，另外也都挺咸的，一顿饭点且只点这两个菜品是最大的失败。
