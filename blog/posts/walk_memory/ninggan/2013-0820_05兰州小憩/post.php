</p>

<p>
晚上我将记录之前几天旅途的博客完成，时间已然不早了，上床睡了一个无压力的觉，醒来已是今晨9点。施施然出门去，无意中发现了那家著名的金鼎牛肉面。昨天其实晚饭也想寻这家店来吃，可惜按地图找到两家分店的所在却均不见影踪。今天本想去另一家牛肉面名店，结果半路倒是看见金鼎了，正是“踏破铁鞋无觅处，得来全不费工夫”。服务员冷冰冰的态度，使这家店在我心中大打折扣。我点了20元的牛肉面，包含两个小菜，一个卤蛋，几片牛肉，如果是10元的，就没有这几片牛肉而只有面里面的牛肉丁了。按牛肉面“一清二白，三红四绿”的标准，这碗面的外观很是不错，面的硬度韧性、汤的鲜度辣度，均恰到好处。我唏哩哗啦两三分钟将面和小菜吃了个光，七分饱，倒是舒坦。你要问兰州拉面？不，兰州人会告诉你不是拉面，是牛肉面。
