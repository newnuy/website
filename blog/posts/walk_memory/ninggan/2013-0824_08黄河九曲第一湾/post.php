<p>
2013.08.24. 雨
</p>

<p>
原本行程的打算是去玛曲县，前天在格桑马队咨询旅行线路时，老板说个人感觉去唐克看黄河九曲第一湾，比玛曲的黄河首曲要更壮观一些。于是我们决定不去玛曲，包车去四川省若尔盖县唐克乡，而途中会经过一个名为花湖的景区。
</p>

<p>
包车的费用需要450元，4座车与7座面包车均为此价。与前天去天葬台的旅友一起，我们已有4人，晚饭前遇到一位面包车师傅，包车费答应便宜到400元，并且说黄河九曲的门票是65元，他可以帮我们便宜到20元。我们4人中唯一的女孩子又在青年旅舍的门口写了找人拼车的信息，又招来3人。不得不承认，女孩子在还价、接洽等沟通工作上有一些先天的优势。
</p>

<p>
唐克在四川，而其实此时所处的郎木寺镇，也并非完全属于甘肃。白龙江将郎木寺一分为二，江北属于甘肃省碌曲县，江南属于四川省若尔盖县。昨天早上，我又扎扎实实地睡了个懒觉，直到要出发前才起床。尽管如此，我还是非常困，因为前天晚上，同住这青年旅舍十人间的有一位年过六旬的老先生，睡着就打呼。我的床头离他的床头是最近的，他打呼不仅声音大，且会突然消失一两分钟又突然呼声大作，极其没有规律。年轻人打呼，往往呼声清澈，这位老先生的呼声中还夹着一丝痰的声音，我在深夜之中听着，更加难以收摄心神。
</p>

<p>
就这样，我直到大约清晨5点才睡着。睡不着的时候，真的想骂人，明知自己打呼这么严重，为了省钱，非要住青年旅舍多人间，就完全不顾别人的睡眠么？所以今天上午坐上去唐克的车，我就开始犯困睡觉。到达花湖景区，门票75元，景区内巴士接送30元，听闻此时花湖并没有多少花，因而只有2人买票进入景区，包括我在内的另外5人在附近转悠消磨时间。一个多小时后，大家回到车上，继续向午饭所在地进军，那就是若尔盖县。由于饿着，睡不着觉，我向同伴请教拍照的一些技巧，在其指导下，我想我明白了之前近景拍不出很好效果的原因。对我的相机也恢复了较多信心，而同伴试过我的卡片机后，居然一致称赞性价比高。
