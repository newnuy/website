<p>
此时天色已晚，大家就近到唐克乡吃了饭，开始了漫长的归途。我利用自身的热量，慢慢烤干了外套，逐渐进入温暖的梦乡。终于在12点时，回到了住宿的地方，真是累得不行了，这天实在是不想再洗澡，迅速洗漱完，栽倒床上。
</p>

<p>
今天早上算是把昨天的困顿都补回来了，接近中午时去吃饭，来到昨天那家云南餐馆，想尝一尝酥油茶和糌粑，这两样藏区最为经典的早餐。可惜已经卖完了，只好点了一份青菜米线，想来云南餐馆的米线应该比较地道。果然并未失望，爽滑的米线、清香的鸡汤，我离开甘南前的最后一餐。一个人坐在店里，看着窗外稀疏的人来人往，任温暖的鸡汤驱除胃里的寒意，真是绝好的慰藉。虽然酥油茶和糌粑由于每天的懒觉而仍未品尝到，但些许遗憾亦可留作下次相见的念想。西藏，想来不用太久，我就会去的。
</p>

