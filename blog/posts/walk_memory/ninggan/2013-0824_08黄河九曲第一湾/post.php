<p>
是的，这照片并没有错，这里的黄河还未流经黄土高原，水清澈着呢。但雨似乎没有要停的迹象，我倒是尿急了。环顾木屋四周，没看到洗手间，看来只能采用绿色环保的方式了。我看准了旁边一座小木屋的侧面，于是走到那里，看到一个牌子上写“严禁随地大小便”。宛如料我机先，当头棒喝，我不禁心中一虚，转身离去，放弃一号行动计划。又过了许久，雨还是不停，有点憋不住了，转念一想，之所以我看准的那个位置立了牌子，可见那就是随地大小便的最佳位置。既然有缘被困于此，又怎么能不去最佳位置体验一下呢？
