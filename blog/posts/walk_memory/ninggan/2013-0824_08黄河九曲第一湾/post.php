<p>
若说我看到黄河九曲第一湾的感受，那首先是对如此清亮澄澈的黄河的惊讶，继而是对大自然构造各种神奇景致的感叹，一条条舞动中的透明绸带镶嵌于青翠广阔的甘南草原上，一眨眼间这一瞬间被定格住，只是绸带的光泽仍沿着绸带在流动闪烁着。这就是黄河九曲第一湾！
