<p>
我们并没有进入若尔盖县城，师傅在边缘的一家小饭店停车让我们吃饭。虽然是师傅指定的小饭店，但还是比较厚道的，菜的口味不错，且价格不高，最让我们称道的就是米饭煮得柔软可口，不像许多高原地区的餐馆的米饭都没熟。之后继续上车向唐克前进，我实在抵挡不住饱餐后的睡意，一路沉睡，直到突然听到车后有人大喊，这才惊醒，回头望去。原来已经到了唐克的黄河九曲第一湾的售票点，师傅趁工作人员不备直接一踩油门，冲进了景区，于是售票的姑娘冲着我们的车顿足怒喝。之前我们也都好奇65元的门票如何优惠到20元，没想到竟是这种方式。
</p>

<p>
当然，看黄河九曲的景致，最好的位置肯定是在山头，景区已经修建了登山的栈道，并在最适合观景处修建平台。而最美的景致出现在日落时分，大约7点，而现在才4点呢。我们转了一下景区内的寺院，而此时正烈日当头，不得不又找了荫凉处坐了一会儿，可又坐得不舒服。倏忽之间，乌云来袭，师傅和几名喇嘛都预测马上要下雨了。
</p>

<p>
我们登上一个小山坡，在呼呼的山风中欣赏了一下黄河九曲的景致，拍了几张照片，但并不想此时登上最高的观景台。所以先选择沿栈道向下，到最接近黄河的岸边去。哪知道行了不到一半，就下起雨来，雨点越来越大，我们只得一路往前，到河边已修建好但尚未投入使用的木屋中避雨，同时算是近距离欣赏黄河雨景。
