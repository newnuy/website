<p>
良久，雨止，一行众人开始沿栈道上行，去等候夕阳。要到最高处，路程可就不短了，行行摄摄中，云层被无力的夕阳撕开的口子又重新缝合，看来最美的景致终究是看不到了。在接近最高观景台处，大雨突至，避无可避的我们，在眺望中匆匆拍了几张照片，就赶忙往山下走去。待得躲进面包车，我已经是外套全湿，他们有人有伞有人戴了牛仔帽，比我稍稍幸运一些。
    <div class="i_center">
