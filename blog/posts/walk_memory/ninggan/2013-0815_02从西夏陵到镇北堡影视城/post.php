<p>
2013.08.15. 晴
</p>

<p>
昨晚睡得极其不好，上铺晃动得厉害，又热醒好多次。老板说，往年银川不会有这么热的，今年很反常，不知是不是现在城市人口急剧增长的结果。
</p>

<p>
早上迷迷糊糊地起床，胡乱吃了点早餐，就准备前往西夏陵。去西夏陵需要坐旅游专线，正好与昨晚也住这家青年旅舍的一位小姑娘同行。经过一个多小时，终于到了期待已久的目的地，在烈日暴晒中进入景区，先去参观博物馆和艺术馆，所谓阵前磨枪，以免到了王陵看得索然无味。
</p>

<p>
博物馆中主要陈列了和西夏相关的一些出土文物等，而艺术馆主要是描绘了西夏人方方面面的雕塑，景区的导游也会做一些简单的讲解。通过艺术馆，能够了解到一些西夏的风俗、文化、礼仪、军事、政治等，无奈心已经飞向大名鼎鼎的王陵了。
