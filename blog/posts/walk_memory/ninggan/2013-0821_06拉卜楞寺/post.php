<p>
2013.08.21. 晴
</p>

<p>
为什么选择去夏河，为什么选择去看拉卜楞寺？如L.P.所说：夏河县城是整个甘南地区的宗教中心，具有西藏拉萨一样的圣城地位。大夏河边、凤山脚下的拉卜楞寺，宏伟壮阔，是甘南藏民心中的大昭寺。
</p>

<p>
昨天傍晚，我到达了夏河，夏河县城并不大，但天色已黑，而L.P.标注的青年旅舍的位置错误，我没看着明显的指示牌，因而颇兜转了一阵，终于找到。这家青年旅舍让我很惊喜，一楼的大厅有着极为浓郁的青年旅舍的氛围。
</p>

<p>
这天晚餐，我在牧民齐全饭庄点了蕨麻饭和石烤肉。蕨麻饭很是特别，在白米饭上撒了蕨麻和白糖，再浇上酥油汁，口味嘛，香甜不腻，算是有特色，但价格也足够对得起名气，小小一碗16元。蕨麻也被称为人参果，这样我也算尝过人参果了。所谓石烤肉，大概是将鸡蛋大的石头放在肉中一起炒出来的，这里选用的是牦牛肉，肉的嚼劲更好一些，也有一些石头的味道。
