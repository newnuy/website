<p>
拉卜楞寺还是电影《天下无贼》的外景地，而要一览拉卜楞寺建筑群的恢宏气势，拉卜楞寺西南方的龙山，登上这不算高的山坡，即可大饱眼福。下午，我和一位来旅行的同学，一起找到那个山坡，登高俯瞰，什么叫占据了夏河县城西部的拉卜楞寺？一下子就有了答案。
