    <img src="/wp-content/uploads/2013/08/gannan001.jpg" title="" /><br />
    蕨麻饭和石烤肉
    </div>
</p>

<p>
今天睡到9点多才起，直接去看拉卜楞寺。拉卜楞寺是藏传佛教格鲁派六大寺院之一，同时又是一所大学，内设六大学院，号称世界最大的藏传佛教学府。不买门票，一样可以随意参观，而40元的门票可以随导游进入一些佛殿。我买了门票，等了足足半小时才等来导游，一位汉语说得很不错的喇嘛。导游领着一大群游客，依次往各佛殿参观。我也记不住导游说的那些和藏传佛教相关的典故，而各殿堂内都不允许摄像，所以挑我印象深刻的记下一笔。
</p>

<p>
围绕拉卜楞寺的转轻道，全长3公里，可能是世界上最长的转经道。喇嘛和信徒会沿着长长的转经道，以虔诚的心，来累积着一世的功德。更虔诚的朝圣者，三步一长头，沿着朝圣之路，而来到殿堂前还要绕殿堂磕长头。信仰的力量第一次深深地震撼了我，简单，却不可磨灭。
