    <img src="/wp-content/uploads/2013/08/gannan002.jpg" title="" /><br />
    喇嘛与信徒转经
    <img src="/wp-content/uploads/2013/08/gannan003.jpg" title="" /><br />
    三步一长头的朝圣者，在殿堂转角处的阳光中显得格外虔诚
    </div>
</p>

<p>
最先参观的是医学院，殿内一侧坐着两排喇嘛，身前放着藏文的纸片，语速急切地念着经，和我们讲求抑扬顿挫的吟诵完全不是一回事。导游介绍，每个殿堂都有前院、正殿、后殿，导游带我们重点参观了后殿，介绍了佛像、唐卡、壁画。拉卜楞寺珍藏了许多年代久远且工艺精美绝伦的唐卡，所谓唐卡，的确不好解释，是一种绘于纸上或布上的画，使用了矿物和植物颜料，色泽鲜艳，千年如昔。
</p>

<p>
在我们退出医学院的时候，看到念经的喇嘛中有正在玩手机的，全然无视我们的关注。走出殿门，等其他游客走远之后，我在禁止摄像的标志之外，向内拍了两张照片，随即被一位巡视的喇嘛喝斥。忙收起相机，去寻找导游，导游刚领着大家从狮子吼佛殿中出来，我正要凭票进去参观时，一位喇嘛关上门对我说：关门了关门了！面对不怎么听得懂汉语的喇嘛，也只能苦笑一下了。赶紧跟上导游，进入酥油花殿，这里面有空调制冷，为的是不让酥油制作的各种物品融化。殿内的佛像、花全都是用酥油制成，因此酥油的气味特别重。其它的殿内的酥油的气味也不淡，因为灯全都是烧的酥油。酥油真是一个神奇的东西，可以吃、可以照明、也可以捏制各类物品。
    <div class="i_center">
    <img src="/wp-content/uploads/2013/08/gannan004.jpg" title="" /><br />
    医学院正殿，来之不易的照片
    </div>
</p>

<p>
酥油花殿旁边就是博物馆，有各种各样的收藏。一位游客问导游，这里的镇馆之宝是哪个？导游说：这里每一件都是镇馆之宝，如果一定要评一件的话，那就是这个猛犸象的象牙。不过，我在L.P.上看到的镇馆之宝是阿底峡赠送的贝叶经和热译师用过的金刚杵。最后，我们来到了大经堂。大经堂正殿有2500平方米，鼎盛时期的拉卜楞寺有4000名喇嘛，而此处可以容纳全寺僧众念经。大经堂中的佛像和壁画，也极为精美。不到大经堂，难以想象拉卜楞寺竟有如此的规模。
</p>

<p>
今天中午在牧民齐全饭庄吃饭，发现来这儿的多是外国人，八人十人一桌的。我点了一杯青稞酒和一份藏式炒面条。青稞酒很清，小喝一口发现是白酒，从不喝白酒的我来品，只能品出酒精味。藏式炒面条没什么特别的，恕我口拙。
    <div class="i_center">
    <img src="/wp-content/uploads/2013/08/gannan005.jpg" title="" /><br />
