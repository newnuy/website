<p>
2013.08.26. 晴
</p>

<p>
前天中午，我从郎木寺镇去兰州。先坐班车到合作市，再坐长途车到兰州。班车到达合作市后，我在长途汽车站门前下车，又恰好买到了去兰州即将出发的长途车的最后一张票，可谓无缝衔接。纵使如此，中午12点离开郎木寺，晚上8点多才到兰州。在甘南坐车，手机往往连通信信号都没有，更别说3G了，又由于颠簸，什么事情也做不了，好在我总是能处之淡然，一个人也不会觉得寂寞。
</p>

<p>
先坐公交到兰州火车站附近，想来肯定宾馆林立。哪知道问了几家，都说已经满了，又走了一段，看到一家什么煤炭招待所，进门看到墙上房间的价格，倒也算比较便宜。但显然工作人员效率低下、态度不佳，还和客人吵了起来，开口就是“我们是三十多年的老店”。我看了一下房间，设施残破、卫生状况堪忧。但时间已经不早，我要了一间，先睡过这一晚再说。
</p>

<p>
简略洗漱之后，睡到床上，在蚊子轰鸣声中沉睡过去。第二天上午，也就是昨天，起床之后，拿上行李，退掉房间，坐公交去紧靠最繁华地段的快捷酒店。10点坐上公交，只有5个站，却足足花了1个多小时，这就是兰州的交通。住进宽敞明亮且干净的房间，第一件事情就是好好洗个澡。这次到兰州，除了作为往河西走廊的中转，另一个重要的目的就是休息。
</p>

<p>
今天下午去看甘肃省博物馆，结果遭遇闭门羹。一向细致的L.P.居然漏写了周一闭馆。因而这两天在甘肃并没有看到任何景点，所以只能分享一些食物了。这回我又去了另一家兰州牛肉面名店，生意总是极好的马子禄牛肉面。风靡全国的兰州拉面，在兰州却不易看到名为“兰州拉面”的餐馆，大街小巷遍布的是兰州牛肉面。我要了一份面、一份牛肉，各7元。虽然汤看起来很是红亮，却是香而微辣，口味很适合我。
