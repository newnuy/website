<p>
香香面皮，最拿手的就是面皮了。我要了一份微辣的，同样被辣得受不了。面皮的口感又有所不同，嚼起来很有面皮的感觉。我知道这样形容很难理解，可是食物的感觉，原本就只是在舌尖。登原汆面总馆，生汆面是许多顾客的最爱。我走进店里，一位七八岁的小姑娘招呼我，问我要什么？我说有菜单吗？回答没有，同时墙上也没有。我只能问，那有什么呢？小姑娘字正腔圆地告诉我这个文盲：生汆（撺）面。好吧，那就生汆面，我来也只是为了生汆面的。所谓汆，是指在开水中稍微煮一下的烹饪方式。生汆面的做法是，一边煮面片，一边往锅里挤丸子。面片比面条，口感当然有所不同，各有所长；丸子脆而嫩，我竟吃不出是什么丸子，但没什么羊肉的味道，店家招牌又有清真字样，想来是牛肉丸子了。
