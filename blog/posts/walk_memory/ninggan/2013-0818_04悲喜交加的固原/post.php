<p>
我赞同固原博物馆是宁夏最值得一看的博物馆的说法，现今如此不适合生活的干涸的西海固地区，竟然留下了那么多辉煌的历史遗迹。请讲解员需要付费，讲解员告诉我，遇到想了解的地方不妨可以试着询问保安，保安听她们讲解多了也都很了解了，后来我发现，此言非虚。而博物馆的藏品之丰富，似乎并不输于银川的宁夏博物馆。旁边还有一个古墓馆，等比例复原了一些各朝代的墓室，令人大开眼界，了解古代墓室的结构，那可并非只是放一具棺材的土坑。可惜，来固原博物馆的游客寥寥无几，尽管是周日。
