<p>
2013.08.18. 晴
</p>

<p>
所谓西海固地区，指的是西吉、海原、固原地区，是中国最干燥的地方。昨天日落时分进入固原市，立即感受到了扑面而来的风沙，再加上汽车站附近正在修路，更是雪上加霜。我步行了一小时去往宾馆，背着有点沉重的行李体会了一把干燥与风沙的蹂躏，果然很不好受。
</p>

<p>
到了宾馆扔下行李，直扑不远的清真名店荣味斋。我点烤羊棒子，服务员说没有，我递过L.P.说这是你们店最受欢迎的菜品，怎么会没有呢？服务员拿着书去询问了一圈，告诉我烤起来比较慢，现在已经不卖了。我颇觉无语，于是点了一份烩羊杂，外加三碗米饭。这烩羊杂微辣，同时羊杂比较原味，符合我的口味，因此我将三碗米饭吃得干干净净。
</p>

<p>
这家宾馆的住宿条件比昨天同心那家好很多，尽管价钱相当。一身疲惫的我好好地洗了个舒服的热水澡，然后就陷入了深深的自责中：在这缺水的西海固地区，我还洗得那么慢！好在自责没有持续太久，我就进入了梦乡。
</p>

<p>
今天我直到中午才起床，退掉房间，把背包寄存在宾馆，准备消磨到晚上10点，再来拿行李去火车站。固原是我在宁夏停留的最后一个地方了，明天一早，我就将出现在甘肃兰州。因此，能否买到宁夏的明信片，全看今天的运气。在一家邮局，我看到了明信片专柜，有好几种宁夏的明信片，特别是有一套名为“塞上江南”，想来应该是宁夏自然风光的汇集。悲剧的是，明信片专柜周日不营业。又来到第二家邮局，这家邮局有“中国集邮”的门面，但又是周日不营业。我还不死心，在邮政营业厅询问，工作人员告诉我，旁边办理快速业务的窗口有卖，但要一小时后工作人员才上班。于是我过了一小时再来，人家告诉我没的卖。
</p>

<p>
有失必有得，固原的餐饮业并不算发达，但特色面食却让我大饱口福。一家名为筋益精的小店，号称有固原最好吃的酿皮。店的外间给顾客用餐，里间是制作、点餐、付钱的所在。一位小姑娘边切酿皮边问我：“你要什么？”我说：“酿皮。还要——”边忙翻开书寻找，“哦，还要牛筋面和燕面糅糅。”小姑娘看着我按图索骥的窘样，莞尔一笑：“燕面糅糅没有。酿皮和牛筋面是分两碗，还是一起拌着吃？”“拌着吃吧。”“能吃辣不？”我看到前面顾客的酿皮被默认放了一大勺子的辣子，倒抽一口凉气，忙说：“少辣，少辣。”可是，真的很辣，不过酿皮确实很凉爽柔嫩。牛筋面粗粗的糙糙的外观，在其它地方未曾见过，吃起来韧性极好，确实有一点牛筋的口感。牛筋面，是我在宁夏遇到的最惊艳的美食。
