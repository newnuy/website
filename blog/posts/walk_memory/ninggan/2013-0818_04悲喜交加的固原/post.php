<p>
幕色降临，风开始大了起来。这边与我所生长生活的江南、南方在夏天的表现不同，江南的夏夜，热风或凉风习习，决不会像这边，让人产生寒意。原本想去唯一的一家德克士坐一会儿，无奈孩童满座，只得在宾馆大厅消磨时光。终于等到10点，前往火车站。哦，这就要离开宁夏了，接下来，就要进入最激动人心的甘肃了。
</p>

