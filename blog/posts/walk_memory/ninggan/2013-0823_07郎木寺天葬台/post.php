<p>
2013.08.23. 雨
</p>

<p>
昨天7点就起床了，因为从夏河县到郎木寺镇的班车只有早上一班。昨晚洗的衣服，也十多个小时了，晾在院子里的铁丝上，居然连袜子都没干。还是用衣架晾衣服会干得快一些，原理嘛，想来是衣架能略微撑开衣服，增大了蒸发的表面积。
</p>

<p>
沿途的风光越来越纯净广阔，到了一处湖边时，驾驶员停车让乘客拍照。这儿就是著名的尕海。尕是小的意思，这个字在藏语中用得较多，读音是嘎的上声。记得许多年前看一部电视剧，警察在西藏追踪逃犯，年迈但生活于高原的逃犯和年轻的警察都已经体力透支时，逃犯“老羊皮”对警察说：小尕娃，你是追不上我的。随后的很长一段时间，我和我爸互相以小尕娃戏称对方。
    <div class="i_center">
    <img src="/wp-content/uploads/2013/08/gannan008.jpg" title="" /><br />
    沿途风光
    </div>
</p>

<p>
接近5小时的车程，终于到达了郎木寺镇，这传说中的“东方小瑞士”，唯一的主街正在疯狂修路，整个镇给我的印象就是大工地。从班车上下来的近一半是旅行者，大家昨天在夏河的青年旅舍也三五成群地认识了。在主街中段找到一家青年旅舍，近二十人纷纷涌入，我则住进了一个十人间，各色室友我会另外撰文描绘。
</p>

<p>
放好行李，已是下午1点，大家一起到隔壁的四川小餐馆吃饭。我点了一份粉条肉丝炒饭，谁料这炒饭一端上台，惊艳全场，在这海拔三千米的所在，米饭、肉丝、粉条全都很入味，已经饿得眼冒金星的我，狼吞虎咽，风卷残云。
</p>

<p>
郎木寺镇的附近，有一些不算出名的景点，比如达仓郎木寺四川寺院、达仓郎木寺甘肃寺院、仙女洞、白龙江源头、纳摩大峡谷、红石崖等。但最吸引我的，无疑是有400多年历史的郎木寺天葬台，一者是想看看天葬台是什么样子的，二者是想登高临远，再给这“东方小瑞士”一次展现身段的机会。
</p>

<p>
走到镇主街的一端，是郎木寺景区的入口，进入景区，就是达仓郎木寺甘肃寺院的各殿堂，之后，步行去3公里外的天葬台。沿路并没有指示，路却也并不难认，但高原的阳光自有额外的威力。虽然是并不陡峭的山路，却由于高原的作用，大家都觉得特别吃力。前行没多久，立即认识到了这次机会没有给错，此时此刻，才能真正把郎木寺镇与瑞士联系起来。环顾四周，所见皆山，有红色岩石的山崖，有白色宛如皑皑白雪的山壁，有翠峰如屏、连绵不绝的山峰，有一碧如洗的蓝天，有纤尘不染的白云，环抱着红瓦白墙、袅袅炊烟的郎木寺镇，澄澈得如同仙境一般。
