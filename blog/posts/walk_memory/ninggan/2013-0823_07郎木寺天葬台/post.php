</p>

<p>
每上行一程，拐过一个弯，就到了一个新的高度，回首望去，即发现视野愈加开阔。当然，风也是越来越大，自己的气息也是越来越喘，同行的旅友问我，你怎么都不喘的啊？尽管我还远远没到体力的极限，但这个海拔的稀薄空气下，又怎么能不喘呢？再拐过一个弯，天葬台出现在了眼前，斜斜的山坡上大片的经幡飘动，整个世界一下子安静了下来，人死后与自然连接的秘密通道就在触手可及的地方！
    <div class="i_center">
    <img src="/wp-content/uploads/2013/08/gannan012.jpg" title="" /><br />
    天葬台
    </div>
</p>

<p>
我们并没有迟疑，一鼓作气爬上天葬台。我在近处看着这些写满符号的经幡，敬畏肃然之心突然消失了，取而代之的是平静和坦然。生死，本就没有什么神秘可言，赤条而来，清白而去。以从容之心行走这一生，却无意留给我们这些不懂生死的旁人刻意探寻的念想。由此来看，我们是有多愚不可及呢！
</p>

<p>
回到镇上，消磨一下时光，又是晚饭的点了。寻寻觅觅中，来到一家当地人推荐的云南餐馆，五个人点了几个菜。个人觉得味道一般，看到墙上贴满的纸条中相当一部分都在称赞牦牛酸奶美味。两位同伴点了一份，尝过之后都说好喝。于是我也点了一份，满怀信心地喝了一口，大失所望：这个酸奶的味道，和以前喝过的“光明”酸奶如出一辙。我在夏河县时也喝过酸奶，那是从来没有体会过的口感，很是鲜美清新无匠气，有一种在翠绿的草原上放完牧数着星星的感觉。有一位同伴同样觉得此刻的酸奶与某些品牌的酸奶口味相同，却就是觉得这个好喝。
    <div class="i_center">
    <img src="/wp-content/uploads/2013/08/gannan013.jpg" title="" /><br />
    夏河县的酸奶
    </div>
</p>

<p>
第二天清晨，天葬台是有天葬的，也有人起早去看了，之后向大家描述了当时的情景：尸体放到地上，一群秃鹫扑食，很快只剩碎骨。我睡了个懒觉，没有特意去看，此事随缘吧。
</p>

