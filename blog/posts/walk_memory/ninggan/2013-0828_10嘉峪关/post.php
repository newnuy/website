<p>
2013.08.28. 晴
</p>

<p>
前天晚上，我从兰州前往嘉峪关。嘉峪关既是一个城市，又是天下第一雄关的名字。晚上我住的四人间的上铺，且无独立卫生间，274元。居然真有人相信了我这句玩笑话，去嘉峪关的火车票买得晚了，买到软卧，正好一夜睡过去。
</p>

<p>
清晨，在火车上醒来，看到窗外的景致，果然已与兰州附近大不相同。一望无际的戈壁，间生的一蓬蓬草，灰蒙蒙的颜色充斥于天地间，喻示着我渐行渐远了。与我同车厢的三位都是嘉峪关人，听闻我此来嘉峪关的目的，纷纷摇头，说嘉峪关城楼有什么好玩的，七一冰川纯属自虐，其中一位大姐甚至推荐我去看本市独树一帜的气象塔。
</p>

<p>
嘉峪关最著名的三个和长城相关的景点：关城，长城第一墩，悬壁长城。所谓关城，大概是“嘉峪关城楼”的简称，因而“嘉峪关市嘉峪关城楼”简称为“嘉峪关关城”，念起来很是奇怪。这三个景点均不易到达，比较合适的方式是包车。我看L.P.上推荐在景点之间徒步或者租自行车去。我好好地找了一下租自行车的地点，但除了手续麻烦的城市公共自行车，实在是没有斩获。总觉得第一墩和悬壁长城对我吸引力不大，但只去关城又太单薄了。午饭吃了一碗干拌牛肉面后，先打车去了长城第一墩。
