<p>
于是，我开始在风中前行，一个人走在公路上，任何时刻放眼四顾，都无法看到行人，最多看到远处的汽车。在风比较小时，我试着打了一个电话，结果对方还是完全听不清我的声音。走着走着，就有一种孤身天地间的感觉，极目远望尽觉一片苍茫，在呼呼的风中似乎以往重要的事物也变得陌生起来。就这样想到了《雪花神剑》的片尾曲《男儿无泪》的几句词：
</p>
<blockquote class="q_kai">
<p>
极目望天边隔断今昔眷恋<br />
剩下独追忆已疲倦<br />
是心中痴怨是不舍不愿<br />
心底的说话早变酸
</p>
</blockquote>
