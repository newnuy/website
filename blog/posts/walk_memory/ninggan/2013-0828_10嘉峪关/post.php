</p>

<p>
到了景区门口，买了门票进去，一条道在戈壁中向前笔直延伸。我沿道前行，十来分钟，在直道的尽头，见到了长城第一墩。这长城沿线建筑规模最壮观、保存最完整的军事关隘，真的就是一个土墩啊，一下子令我倍感失望。看了一下四处也没发现景区内的其它去处，就开始筹划去关城。而就这样恰恰错过了第一墩最精彩的讨赖河峡谷和百米吊桥。问了几辆随游客而来的出租车，接下来都不是去关城，看来蹭车的想法也破灭了，只能享受一下L.P.所说的徒步了。从此沿公路到关城，大约8公里，要走2小时。
