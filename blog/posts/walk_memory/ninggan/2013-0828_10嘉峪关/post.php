<p>
现代的交通和通讯已经足够快捷，遥想古代，若一人身处此境，又是一种什么样的感受呢？金庸在《书剑恩仇录》中描绘了主人公陈家洛从嘉峪关向回疆赶路的情景：
</p>
<blockquote class="q_kai">
<p>
不一日已到肃州，登上嘉峪关头，倚楼纵目，只见长城环抱，控扼大荒，蜿蜒如线，俯视城方如斗，心中颇为感慨，出得关来，也照例取石向城门投掷。关外风沙险恶，旅途艰危，相传出关时取石投城，便可生还关内。行不数里，但见烟尘滚滚，日色昏黄，只听得骆驼背上有人唱道：“一过嘉峪关，两眼泪不干，前面是戈壁，后面是沙滩。”歌声苍凉，远播四野。一路晓行夜宿，过玉门、安西后，沙漠由浅黄逐渐变为深黄，再由深黄渐转灰黑，便近戈壁边缘了。这一带更无人烟，一望无垠，广漠无际，那白马到了用武之地，精神振奋，发力奔跑，不久远处出现了一抹岗峦。
</p>
<p>
转眼之间，石壁越来越近，一字排开，直伸出去。山石间云雾弥漫，似乎其中别有天地，再奔近时，忽觉峭壁中间露出一条缝来，白马沿山道直奔了进去，那便是甘肃和回疆之间的交通孔道星星峡。峡内两旁石壁峨然笔立，有如用刀削成，抬头望天，只觉天色又蓝又亮，宛如潜在海底仰望一般。峡内岩石全系深黑，乌光发亮。道路弯来弯去，曲折异常。这时已入冬季，峡内初有积雪，黑白相映，蔚为奇观，心想：“这峡内形势如此险峻，真是用兵佳地。”过了星星峡，在一所小屋中借宿一晚。次日又行，两旁仍是绵亘的黑色山岗。奔驰了几个时辰，已到大戈壁上。戈壁平坦如镜，和沙漠上的沙丘起伏全然不同，凝眸远眺，只觉天地相接，万籁无声，宇宙间似乎唯有他一人一骑。他虽武艺高强，身当此境，不禁也生栗栗之感，顿觉大千无限，一己渺小异常。
</p>
</blockquote>

<p>
我是绝不可能比金庸写得更精彩了，尽管怀疑这些地方金庸在写《书剑恩仇录》的而立之年是否亲临过。在这公路上默默地走着，一片荒凉中突然一回眸，发现了路边沙砾中竟然盛开着一株鲜艳的花朵，迎风摆动，不畏风沙，心里不由生出一丝异样的感觉。难怪说很多人到了西北就突然决定结婚，我这一刻也有了结婚的念头。待我真的见到关城时，除了感叹城楼宏伟，却并非那么容易想象古时出关的情思。然凭城而立，不得不联想到古时争战之残酷，花费偌大人力修建此关，所谓一夫当关，万夫莫开，纵使强攻弱守，也必折损甚巨。一将功成万骨枯，指的又岂止这一个领域？
