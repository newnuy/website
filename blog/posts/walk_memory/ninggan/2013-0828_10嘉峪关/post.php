<p>
今天就完全在休整之中度过，什么别的事情也没做，除了吃。我又用嘴了解了一种羊肉烹饪方法：清炖。这种烹饪方法的原味保持得比较好，但没有蒸的鲜。按惯例一斤，再来两碗米饭，今晚再睡个好觉，明天可以去七一冰川了。
