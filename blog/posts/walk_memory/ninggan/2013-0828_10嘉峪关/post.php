<p>
晚上，我回到市区，寻到传说中的富强市场，要了一碗浆水面，5串烤羊肝和5串烤羊脖肉。浆水面嘛，确实有点浆水的感觉，我不是很喜欢。羊脖肉吃起来没觉着特别，但羊肝的确极其鲜嫩。原计划第二天也就是今天早起去七一冰川，临睡时觉得精神极其不佳，因而一觉睡到中午，决定休整一天。
