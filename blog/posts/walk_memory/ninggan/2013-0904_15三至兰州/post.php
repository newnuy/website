<p>
从博物馆出来，坐车前往白塔山，这是兰州城内颇有名气的登高之处。登白塔山，山倒不高；访白塔寺，寺正修缮。依山凭寺，隔江远眺，两岸夹峙，一水中分，那正是数千年来灌溉华夏文明的母亲河，也正是前些日在唐克<a href="/?p=1284" target="_blank">九曲第一湾</a>见到的澄澈透亮、宛如玉带的黄河！凝望着浩浩汤汤的黄河水东流不息，而南北飞架其上的正是超过百年历史、由德国制造的中山铁桥，前者动，后者静，似在一起诉说着自然的变迁与人类的延革。蓦地，忆起了李白《将进酒》的诗句：
</p>
<blockquote class="q_kai">
<p>
君不见黄河之水天上来
奔流到海不复回
君不见高堂明镜悲白发
朝如青丝暮成雪
</p>
</blockquote>

