<p>
2013.09.04. 雨
</p>

<p>
昨天上午继续九点出发，离开寒冷的青海湖畔，大家面露喜色地前往兰州。四百公里的车程，应该不会像前天那么难走。只是得饿着肚子上路了。
</p>

<p>
下午两点，大家靠着高速公路上超市中买来的零食撑到了兰州。由于我和昭将在兰州等待各自的回程，静雯则要率猪儿、小喆取道陇南，继续向四川而去。在连霍高速兰州入口处，有一家阿西娅羊羔肉，号称兰州最好的黄焖羊肉店，无疑是大家最后的午餐的首选之地。这家饭店很大，生意相当火爆，我们四人先入座点菜，静雯则在车上稍稍补充一下精神，接下来往陇南的路可不容易。
</p>

<p>
吃吃聊聊，尝过贵而量少的黄焖羊肉，大家就该分道扬镳了。在旅途中，分离才是主调，相聚从来不会长久。好在今朝不似古时，尚有便捷的交通和实时的通讯。我和昭打车到花儿青年旅舍，住男女混住十人间。过了晚饭时间，我们与刚入住的一位姑娘小全同去吃饭，在旅舍旁的一家自助火锅店吃个了撑。我和小全约了第二天一同去甘肃省博物馆。
</p>

<p>
今天上午，和小全一起去博物馆。甘肃省博物馆展品数量之多、级别之高，令人目不暇接。但历史的匮乏，使我只能是走马观花，看个热闹。其中汉朝的铜奔马，也就是曾选入小学课文的《马踏飞燕》的来源，自然引众多参观者啧啧围观，但咨询馆内工作人员，得知此时展出的是赝品。
