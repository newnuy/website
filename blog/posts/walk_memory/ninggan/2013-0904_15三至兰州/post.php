<p>
光阴如棱，回到兰州，此次旅程也接近了尾声。而人生，也仿佛是经历着一次次的旅行，急急流年，滔滔逝水，又怎能不令人感慨万千？《倚天屠龙记》中，在身陷绝境之时，小昭笑吟吟地唱起了曲子：
</p>
<blockquote class="q_kai">
<p>
世情推物理，人生贵适意，
想人间造物搬兴废。
吉藏凶，凶藏吉。
</p>
<p>
富贵哪能长富贵？
日盈昃，月满亏蚀。
地下东南，天高西北，天地尚无完体。
</p>
<p>
展放愁眉，休争闲气。
今日容颜，老于昨日。
古往今来，尽须如此，
管他贤的愚的，贫的和富的。
</p>
<p>
到头这一身，难逃那一日。
受用了一朝，一朝便宜。
百岁光阴，七十者稀。
急急流年，滔滔逝水。
</p>
</blockquote>

<p>
晚饭本想去小吃街饱餐一顿，不料突然下起大雨来，顿时没有兴致，索性打道回府。倒是在青年旅舍附近尝了所谓的卷子鸡，其实就是宽面条炒鸡块，说成卷子倒也贴切。
