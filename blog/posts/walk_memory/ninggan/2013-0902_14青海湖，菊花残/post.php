<p class="vblank"></p>

<p>
2013.09.02. 阴
</p>

<p>
昨晚在两位男士的呼声中渐渐入睡，好在一夜无梦。今天上午，按静雯九点出发的习惯，集合准备前往青海湖。今天行程五百公里，且路况比昨天要差很多，大家决定先吃午饭再上路。找到一家餐馆，点了几个菜，大家并不算迅速的填饱肚子，接下来的辛苦就交给静雯了。
</p>

<p>
开始的路还算平直，两小时后，我们在一风景优美之处停车休憩，拍照远眺，呼吸寒冷的空气。接着又继续行车，沿途不时能看到牧人赶着羊群或牦牛，在山坡或公路上行走。遇到牦牛群挡路，我们当然得减速等待，说实话我当时心里是惴惴不安的。
    <div class="i_center">
    <img src="/wp-content/uploads/2014/02/qinghaihu001.jpg" title="" /><br />
    公路上的羊群
    <img src="/wp-content/uploads/2014/02/qinghaihu002.jpg" title="" /><br />
    公路上的牦牛群
    </div>
</p>

<p>
渐渐地，我们落入了祁连山的包围中，攀高伏低的开车不再是愉快的事情。静雯开始犯困，坐在副驾驶的小喆自称开车很稳，想替静雯开一会儿，静雯说从来不让别人开自己的车，因为一旦出了事情责任上会很麻烦。后来，静雯实在困得很，换小喆开，她则至副驾驶小睡一会儿。小喆开车确实很稳，在他平稳的行驶中，渐觉昏昏然。又过一会儿，竟行至一处景致绝佳所在，狭窄的公路两侧，一为峭壁一为悬崖，峭壁上潺潺溪水泻下，直落悬崖。峭壁之上，野花发而幽香；莺莺燕燕之声，不绝于耳。临崖而观，虽不寒而栗，却也为广阔天地、青山绿水所倾倒，登感襟怀大畅。而今天的旅舍，就建在这如屏峭壁之上，凭窗四顾，犹在空中，妙不可言。
</p>

<p>
突然，旅舍消失了，峭壁、悬崖、野花、溪水、飞鸟，全都消失了。睁开眼来，原来刚才是在梦中，还是如此美妙的梦。看到已换回静雯开车，看看时间，我并没有睡很久。我向大家描述了刚才的梦境，居然有人说，你是把我们刚才谈论的内容做到梦里去了吗？小喆说，刚才经过的最高的地方有海拔4100米，令我非常惊讶。
    <div class="i_center">
    <img src="/wp-content/uploads/2014/02/qinghaihu003.jpg" title="" /><br />
    接近青海湖的景致
    </div>
</p>

<p>
终于，路慢慢变得平直，傍晚时，我们看到了青海湖。许多人到青海湖是为了看油菜花，而现在油菜花已经谢了，当然，来看一眼中国最大的内陆湖及最大的咸水湖，也是一件令人开心的事情。而湖边有当地人搭的帐篷和马匹，让我们萌生了骑马观湖的想法。停下车，和一位阿姨谈好价格，她牵出五匹马，让我们上马去湖边。
    <div class="i_center">
    <img src="/wp-content/uploads/2014/02/qinghaihu004.jpg" title="" /><br />
    青海湖
    </div>
</p>

<p>
这是我第一次骑马，才知道拍武打片也不是一件容易的事情。马本身就挺高的，马镫沉重也很难踩稳，时刻担心不小心被颠下来。当马不想走时，用力夹马肚它也最多勉强前行几步，又很快驻足。当马由小碎步慢慢加速小跑时，我不禁害怕起来，赶紧勒缰绳让它停下来，不敢想象如果它发足狂奔会是什么情景。当马想转头回帐篷那边时，又得拼命往反方向拽缰绳。好不容易兜兜转转把马哄到几里外的湖边，下马休息观湖，等日落。
</p>

<p>
湖边风大，我已经抵受不住，而众人情绪高涨，我又不敢独自策马回程，只好和大家一起等待日落的降临。湖面辽阔，缈不见人，水波粼粼，金光闪闪。夕阳随着缓缓下降，渐远渐暗，光转昏黄，未至湖面即掩身云层之后，顿时四下夜意袭来。在等日落的时候，大家互相帮忙拍照，其中一位台湾的男孩昭，每次帮他拍照时，他虽没摆什么特别的架势，却自然而然显得很酷。我说，我发现你很懂摆拍哎，你的造型和动作，都比一般的男孩子要酷很多，是不是常去夜店啊？昭说，你不要污蔑我是夜店咖。好吧，昭为自己起了个很不错的外号。之后闲时，我就问他：夜店咖，到了兰州，还有几天时间呢，带我去夜店耍耍吧？
