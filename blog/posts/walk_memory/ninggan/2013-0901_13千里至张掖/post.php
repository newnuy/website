<p>
大佛寺这个名字，倒让我想到以前看《少年包青天》电视剧时，其中的一副对联：
</p>
<blockquote class="q_kai">
<p>
客上天然居，居然天上客<br />
人过大佛寺，寺佛大过人
</p>
</blockquote>
<p>
当然，这些都是历史上的名对，并不是编剧的原创，还有一联是：<span class="q_kai">僧游云隐寺，寺隐云游僧。</span>
</p>

<p>
到了晚饭时间，离开大佛寺，回到宾馆，静雯已经睡醒，由于中午没吃午饭，两人都饿得不行了，正好出去吃晚饭。猪儿一众也正好从马蹄寺回到市区，找了一家当地人推荐的手抓羊肉店，于是我俩顶着饥饿，找到那家店，众人围坐一圆桌，风卷残云地吃了个饱。极饿之后，吃什么都很香，再说这手抓确实非常入味。
</p>

<p>
晚饭过后，大家回宾馆。我和静雯走在一起，静雯说，我请你喝杏皮水，我说，还是我请你吧。静雯说，我现在能请得起你的，也只有这个了。我不禁失笑，人的一生中，有多少次是选择了拥有很多却给你很少的人，又有多少次是选择了拥有很少却仍愿给你的人。静雯又说，你没见过我非常大方的时候，在成都，请朋友吃饭，我把菜单给他们，说，随便点，我请客！那神情，很是豪爽。
</p>

<p>
到了宾馆，换过房间，我和两个男孩同住三人间。这两位同样误会静雯和我是一对，静雯倒是乐得被误解，说这样可以省去旅途中来自别人的不少麻烦事。
